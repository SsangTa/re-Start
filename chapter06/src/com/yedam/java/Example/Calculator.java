package com.yedam.java.Example;

public class Calculator {
	
	//더하기
	int plus(int x, int y) {
		int result = 0;
		result = x + y;
		return result;
	}
	
	//빼기
	int min(int x, int y) {
		int result = 0;
		result = x - y;
		return result;
	}
	
	
	//곱하기
	
	double multi(double x , double y) {
		double result = x * y;
		return result;
	
	}
	//나누기
	double divide(double x , double y) {
		double result = 0;
		result = x / y;
		return result;
	}
	

}
